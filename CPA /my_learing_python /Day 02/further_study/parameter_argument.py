"""
[주제]
함수에서 매개변수(Parameter)와 인수(Argument)의 차이

[개념 설명]
파이썬 함수를 다룰 때 가장 혼동하기 쉬운 개념 중 하나가 '매개변수'와 '인수'입니다.
이 둘은 밀접하게 관련되어 있지만, 사용되는 시점과 역할이 다릅니다.

1.  **매개변수 (Parameter)**:
    * **정의**: 함수를 **정의할 때** 괄호 안에 나열하는 변수명.
    * **역할**: 함수 내부에서 사용될 '입력 값의 이름' 또는 '자리표시자(placeholder)'. 함수가 어떤 종류의 데이터를 받을 것인지 선언하는 역할.
    * **예시**: `def greet(name):` 에서 `name`

2.  **인수 (Argument)**:
    * **정의**: 함수를 **호출할 때** 매개변수에 실제로 전달하는 값.
    * **역할**: 매개변수에 할당되어 함수 내부로 전달되는 '실제 데이터'.
    * **예시**: `greet("강민석")` 에서 `"강민석"`

[비유]
매개변수는 '빈 상자'의 이름(예: '이름 상자')이고, 인수는 그 '상자에 실제로 담는 물건'(예: '김민준'이라는 이름표)이라고 비유할 수 있습니다.

[활용 팁]
이 두 용어를 정확히 이해하면 함수를 설계하고 사용할 때 어떤 값을 어떻게 전달해야 하는지 명확하게 파악할 수 있습니다. 함수의 유연성과 재사용성은 매개변수와 인수를 통해 구현됩니다.
"""
