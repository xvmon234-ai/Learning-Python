"""
[주제]
파이썬에서 들여쓰기의 역할과 중요성

[개념 설명]
파이썬에서 들여쓰기는 단순한 코드 스타일이 아닌, 코드 블록의 범위를 정의하는 **문법적 요소**입니다. 다른 프로그래밍 언어에서 중괄호(`{}`)를 사용하는 것과 유사한 역할을 합니다. 올바른 들여쓰기는 코드의 실행 흐름을 결정하며, 잘못된 들여쓰기는 `IndentationError`를 발생시킵니다.

[학습 기록]

- 학습 질문:
  - 최초 코드 작성 시 'else' 문에 들여쓰기를 하지 않아 'if' 블록에 포함되는 문법 오류가 발생했습니다.
  - 이로 인해 파이썬 인터프리터가 'else'를 예상치 못한 구문으로 인식하고 `IndentationError` 또는 `SyntaxError`를 발생시켰습니다.

- 문제 해결:
  - 파이썬의 조건문(if/else)에서 **코드 블록은 반드시 들여쓰기(Indentation)로 구분**된다는 점을 다시 상기했습니다.
  - 'else' 키워드가 'if'와 같은 들여쓰기 수준에 오도록 수정하여 문제를 해결했습니다.

- 추가 학습:
  - 파이썬은 중괄호({}) 대신 **들여쓰기를 사용하여 코드의 종속성(scope)을 결정**한다는 핵심 규칙을 다시 한번 체득했습니다. (예: `if` 다음의 들여쓰기된 부분은 `if`의 영향을 받고, `else` 다음의 들여쓰기된 부분은 `else`의 영향을 받음)
  - 들여쓰기 오류는 파이썬에서 가장 흔하게 발생하는 초기 오류 중 하나이며, 이는 파이썬 문법의 핵심이라는 것을 이해했습니다. 향후 코드 작성 시 들여쓰기에 더욱 유의해야겠습니다.

  [활용 팁]
-   `if`, `elif`, `else`, `for`, `while`, `def`, `class` 등 콜론(`:`)으로 끝나는 구문 다음에는 반드시 들여쓰기된 코드 블록이 와야 합니다.
-   일반적으로 파이썬에서는 **공백 4칸**을 들여쓰기 표준으로 사용합니다. 일관된 들여쓰기 방식을 유지하는 것이 중요합니다.
"""
