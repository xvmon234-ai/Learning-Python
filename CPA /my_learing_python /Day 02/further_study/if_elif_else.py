"""
[주제]
if-elif-else 문의 순차적 조건 검사

[개념 설명]
`if-elif-else` 문은 조건을 위에서부터 아래로 순차적으로 검사합니다.
-   `if` 조건이 참(True)이면 해당 블록의 코드를 실행하고, 나머지 `elif`와 `else` 블록은 **모두 건너뜁니다.**
-   `if` 조건이 거짓(False)이면 다음 `elif` 조건을 검사합니다.
-   모든 `if`와 `elif` 조건이 거짓이면 `else` 블록의 코드를 실행합니다.

[예시]
성적 계산기 코드를 통해 이해:

성적: 95점
1. if grade >= 90: (True) -> "A 학점입니다." 출력. 이후 elif, else는 실행되지 않음.

성적: 85점
1. if grade >= 90: (False)
2. elif grade >= 80: (True) -> "B 학점입니다." 출력. 이후 else는 실행되지 않음.

성적: 65점
1. if grade >= 90: (False)
2. elif grade >= 80: (False)
3. elif grade >= 70: (False)
4. else: (실행) -> "F 학점입니다." 출력.

이처럼, 'elif grade >= 80'에 도달했다는 것은 이미 'grade < 90'이라는 것이 전제되어 있다는 의미임.
따라서, 'elif 90 > grade >= 80'과 같은 복합 조건은 논리적으로는 맞지만, 'elif grade >= 80'만으로도 충분하고 더 간결함.

[활용 팁]
-   이러한 순차적 특성 때문에, 이미 앞선 조건에서 걸러진 내용은 `elif`에서 다시 명시할 필요가 없어 코드를 더 간결하고 효율적으로 작성할 수 있습니다.
    예: `if score >= 90:` 다음 `elif score >= 80:`은 자동으로 `score < 90`이라는 조건을 내포합니다.
"""

